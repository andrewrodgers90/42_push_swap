#include "push_swap.h"

void	reverse_rotate(t_stack **stack)
{
	t_stack	*last;
	int		len;

	len = stack_size(*stack);
	if (NULL == stack || NULL == *stack || len == 1)
		return ;
	last = stack_last(*stack);
	last->previous->next = NULL;
	last->next = *stack;
	last->previous = NULL;
	*stack = last;
	last->next->previous = last;
}

void	rra(t_stack **a)
{
	reverse_rotate(a);
	write(1, "rra\n", 4);
}

void	rrb(t_stack **b)
{
	reverse_rotate(b);
	write(1, "rrb\n", 4);
}

void	rrr(t_stack **a, t_stack **b)
{
	reverse_rotate(a);
	reverse_rotate(b);
	write(1, "rrr\n", 4);
}
