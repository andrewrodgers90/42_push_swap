#include "push_swap.h"

void	rotate(t_stack **stack)
{
	t_stack	*last;
	int		len;

	len = stack_size(*stack);
	if (NULL == *stack || NULL == stack || 1 == len)
		return ;
	last = stack_last(*stack);
	last->next = *stack;
	*stack = (*stack)->next;
	(*stack)->previous = NULL;
	last->next->previous = last;
	last->next->next = NULL;
}

void	ra(t_stack **a)
{
	rotate(a);
	write(1, "ra\n", 3);
}

void	rb(t_stack **b)
{
	rotate(b);
	write(1, "rb\n", 3);
}

void	rr(t_stack **a, t_stack **b)
{
	rotate(a);
	rotate(b);
	write(1, "rr\n", 3);
}
